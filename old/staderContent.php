		<img class="stadbild" src="img/city/therike.png" title="The Rike">
		<h2>The Rike<img class="miniflag" src="img/flag/castle.png"></h2>
		  <p>
		  Grundare: Carl<br>
		  Grundades: April 2013<br>
		  Storlek: By<br>
		  Biom: Forest, Taiga, Jungle<br>
		  Kust: Nordkust<br>
		  Mur: Cobblestone, Stonebricks, 4 torn (Klar)<br>
		  Choords: -200; -1550
		  </p>
		  <p>
		  <b>The Rike</b> grundades i tidig historia av Carl, som ett hus för
		  gruvarbete en bit ifrån Fabulania för att slippa stöta på gamla
		  gruvgångar. Efterhand började Carl även skaffa ved till Fabulanias
		  invånare och skogen det tidiga The Rike var belagd i blev till en
		  gräsplatå på kort tid.
		  Sakta växte staden fram och nu är det den mysiga träby ingen trodde det
		  skulle bli och har nu även utväklats till ett ställe för Team 1224, en
		  av LeCaNis crews, därbland Nicke, Carl och Oskar.
		  The Rike blev även den första staden med fulla murar.<br>
		  Efter över ett år av aktivitet i staden blev varenda lilla trähus uppbränt
		  i ett grief och Carl, som hade byggt upp staden, flyttade till Bergavik för
		  att fortsätta livet på LeCaNi. Med honom följde både Nicke och Oskar och
		  The Rike lämnades griefad i en lång tid.<br>
		  Efter många långa månader i damm kom Carl tillbaka till staden i Juni 2015 och
		  reparerade många av husen, inte för att flytta dit men för minnens skull.
		  Byn är nu fullt funktionell och ser ut som den tidigare gjort, men utan invånarde.
		  </p>
