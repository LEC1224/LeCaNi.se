		  <b>New Earth</b> var ett sätt att väcka aktivitet på LeCaNi och lyckades. I ett par månader
		  spelade många på servern och alla bodde ihop. Affärer öppnades och gemenskapen stärktes på
		  ett sätt LeCaNi inte varit med om under lång tid tillbaka. Efter några månader griefades en
		  stor del av staden och nu står bara runt hälften av husen kvar och tillsammans med ruinerna
		  av andra hus bildar de en mystisk spökstad bärandes på många, korta minnen i en av LeCaNis
		  många skogar.
