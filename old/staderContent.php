		<h2>Vad är en stad?</h2>
		<p>
		Jo, varje samhälle med tio eller fler hus som ligger minst femhundra
		block från närmsta stad räknas som stad. Städer kategoreras sedan in i
		mindre grupper. En stad med 10 till 19 hus räknas som by (till exempel
		The Rike), en stad med 20 till 49 hus räknas som vanlig stad (till
		exempel: New Earth), en stad med 50 till 99 hus räknas som storstad
		(till exempel: Fabulania) och om en stad har mer än 100 hus så räknas
		den som en Jävligt Stor Stad. Ingen stad har någonsin fått den titeln i LeCaNis historia. Har du en
		stad du vill registrera här? På "server"-sidan finns en "Grunda en
		stad"-flik. Här nedan ser ni en lista på alla registrerade städer!
		</p>
