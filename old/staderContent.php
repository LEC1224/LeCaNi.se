		<div class="city">
		  <img src="img/city/bergavik.png" title="Bergavik">
		  <h2>Bergavik</h2>
		  <p>
		  Grundare: Carl, Nicke<br>
		  Grundades: November 2013<br>
		  Storlek: Vanlig Stad<br>
		  Biom: Extreme Hills<br>
		  Kust: Östkust<br>
		  Mur: Stonebricks, Dark Oak, 6 torn (Klar)<br>
		  Choords: 1650; -4000
		  </p>
		  <p>
		  <b>Bergavik</b> skiljer sig från alla andra städer då den är byggd under två massiva berg.<br>
		  Stadens första byggnader var Carls fiskestuga och Nickes Dojo en bit från New Earth där de
		  kunde vara själva men nära till civilisationen. Efter att spelat där några veckor så fick de
		  idén att bygga ett stort ihåligt berg där de kunde bygga en stad. Den 24 November 2013 började
		  de bygga berget och blev klara redan dagen därpå men lyckades även ta slut på all sten de hade samt
		  all sten Malle125 hade.<br>
		  Sen dess har grottostaden vuxit. Ett till berg har byggts och det första har byggts ut och blivit
		  dubbelt så stort. Nu bor Carl där på deltid och Nicke de få stunder han spelar.
		  </p>
