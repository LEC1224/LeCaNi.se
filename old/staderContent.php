		  <img class="stadbild" src="img/city/vildtmarken.png" title="Vildtmarkens broar och Frihetsvillager">
		  <h2>Vildtmarken</h2>
		  <p>
		  Grundare: Carl, Josef<br>
		  Grundades: November 2022<br>
		  Storlek: By<br>
		  Biom: Plains, Oak forest, Dark forest, Hills<br>
		  Kust: Nej<br>
		  Mur: Nej<br>
		  Choords: 8000, -9000
		  </p>
		  <p>
		  <b>Vildtmarken</b> skapades i syfte att nå nygenererad terräng som uppkommit både i Overworld och Nether. Carl började bygga en väg i Nether som gick tusen block i vardera axel, och således gått en bit in i nygenererad netherterräng. Där byggde han och Josef en portal och <a href="https://youtu.be/-PImgXHmeEY"> utforskade terrängen</a> de nådde. De båda var mycket nöjda med naturutbudet och grundade en ny stad där.
		  </p>
		  <p>
		  Namnet på staden syftar till den mer glesbebyggda stadsform Vildtmarken tagit, där spelare valt att bygga hus längre ifrån varandra och skapa mer utrymme för tomter, stigar och skog emellan husen. Denna byggstil skiljer sig från t.ex. Fabulanias mer begränsade utrymme inom sina murar, där ett tydligt centrum och vissa tätbebyggda stadsdelar växt fram. Namnet myntades i discord av Carl, Martin och SpX när staden var mycket ung.
		  </p>
		  <p>
		  Sedan grundning till skrivande stund (Mars 2023) har Vildtmarken varit en katalyst för spelintresse på servern och aktiva spelare, såsom SpX och MajaGustav byggde där, och många nya spelare, som kommit in efter Vildtmarkens grundande, har valt att börja sin resa på LeCaNi i Vildtmarken.
		  </p>
