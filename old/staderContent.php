		  <img class="stadbild" src="img/city/fabulania.png" title="Fabulania från väster">
		  <h2>Fabulania<img class="miniflag" src="img/flag/leontania.png"></h2>
		  <p>
		  Grundare: Andreas, Carl-Johan<br>
		  Grundades: Mars 2013<br>
		  Storlek: Storstad<br>
		  Biom: Plains, Jungle, Extreme Hills<br>
		  Kust: Västkust<br>
		  Mur: Stonebricks, Oakplanks, 8 torn (Klar)<br>
		  Choords: 0; -900
		  </p>
		  <p>
		  <b>Fabulania</b> är serverns huvudstad och den största staden som
		  funnits i LeCaNis historia. Den grundades i Mars 2013 av xxSlaktarn och
		  LilleAndrew. Därför är detta den enda staden som funnits med unte hela
		  LeCaNis tid (den här världen). Den har vukit mycket, för det mesta i
		  träbyggen men även ett och annat stenhus tittar fram.
		  </p>
		  <p>
		  Fabulanias Mur påbörjades i Maj 2013 och avklarades tidigt i Februari 2018. Med en byggtid på närmare fem år är detta det längst pågående projektet någonsin på LeCaNi, med X-Mines på en blygsam andraplats med drygt tre års arbetstid.
		  </p>
