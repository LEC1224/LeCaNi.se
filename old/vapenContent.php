	<h1>LeCaNis Stadsvapen</h1>
</div>
<div class="content">
	<p>
	Vissa städer på LeCaNi har ett 4x4-pixlar emblem, eller ett "stadsvapen". Några av dessa märken har en längre historia än staden själv, så jag kände att de kunde få en egen sida här på hemsidan.
	</p>
	<div class="vapen">
		<h2>Leontanias Stadsvapen</h2>
		<p>
		<img class="flag" src="img/flag/leontania.png">
		Leontanias stadsvapen, bestående av två 'L', ett spegelvänt horizontellt och ett vertikalt, uppstod 2011 på en av LeCaNis tidigare världar. Då tillhörande huvudstaden Leontania, döpt efter Leonta, döpt efter Leo. Detta var LeCaNis första stadsvapen och inspirerat av 90gQs Nirethia-märke. Efter några år har detta märket omformats från att bara vara ett stadsvapen till att vara en ikon för LeCaNi och de två L-en har inofficiellt bytt betydelse från förkortning av Leontania till LeCaNi. Detta märket kan man bland annat hitta på server-ikonen när man loggar in på LeCaNi, i texturepack-ikonen och i webbläsarfliken för lecani.se!
		</p>
		<p>
		Trots att märket inte längre bara är ett stadsvapen betyder det inte att vi inte använder det som stadsvapen ändå. I nuläget är huvudstaden Fabulania tilldelad märket som sitt stadsvapen och går att hitta i guld vid murens portar och i trä på hörntornen.
		</p>
	</div>
	<div class="vapen">
		<h2>Castle Towns Stadsvapen</h2>
		<p>
		<img class="flag" src="img/flag/castle.png">
		Näst älds är Castle Towns stadsvapen. Detta märke gjorde sin deput på samma gamla värld som Leontanias märke men i staden Castle Town, grundad av Carl. Märket ska symbolisera just ett slott, eller snarare mur, med de två övre prickarna som skyttegluggar, och de två nedre hålen som fönster, med andra pixelraden helt tom för att göra märket lite luftigare.
		</p>
		<p>
		I nuläget är märket stadsvapen till The Rike, en liten by nord-väst om Fabulania grundad även den av Carl.
		</p>
	</div>
	<div class="vapen">
		<h2>Odöpta Fortets Stadsvapen</h2>
		<p>
		<img class="flag" src="img/flag/fortet.png">
		Odöpta Fortets stadsmärke uppkom betydligt senare än de tidigare nämna, nämligen 2014 som stadsvapen för just Odöpta Fortet, på nuvarande värld. Detta märket bär alltså inte på någon längre historia.
		</p>
		<p>
		Vapnet är format efter det matematiskt signifikanta talet pi.
		</p>
	</div>
	<div class="vapen">
		<h2>Döpta Fortets Stadsvapen</h2>
		<img class="flag" src="img/flag/castle2.png">
		<p>
		Döpta Fortets stadsvapen är ett relativt ungt märke, först uppenbarat i Februari 2018 som märke för Odöpta Fortets "elake tvillingbror", Döpta Fortet.
		</p>
		<p>
		Till skillnad från de ovanstående stadsvapnen har detta ingen betydelsefull symbolik i sitt utseense. Utan är helt enkelt Leontanias märke där man tagit pixelbiten från plats (4,3) och lagt på (3,1).
		</p>
	</div>
