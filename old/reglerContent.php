	<h2>Rättigheter</h2>
	<p>Varje spelare har rätt till att gå in i spectator-läge så länge denne inte använder spelläget för bekväm transport.
	</p>
	<p>Varje spelare har rätt till att få sin spelardata tillbakadragen och på så sätt återfå sina saker om denne dött och förlorat sina saker på grund av bugg eller lagg.
	</p>
	<p>Varje spelare har rätt att grunda en egen stad och även bestämma över sin stad till den grad att det inte överskrider LeCaNis principer.
	</p>

	<h2>Skyldigheter</h2>
	<p>Varje spelare måste få alla sina resurser genom metoder som finns tillgängliga i multiplayer survival minecraft.
	</p>
	<p>Varje spelare ansvarar för att behålla naturen vacker om denne är inom 500 block från en stad (undantag för avsedda resurstillgångar så som stenbrott, sandbrott, farmer).
	</p>
	<p>Tweetar med hashtagen #lecani måste vara relevanta för servern eller LeCaNis gemenskap.
	</p>
	<p>Varje spelare måste vara vänlig mot andra spelare.
	</p>
	
	<h2>Buggar och externa verktyg</h2>
	<p>Minecraft har alltid haft buggar som gjort att saker som inte borde funka i Survival Mode ändå blivit möjliga. T.ex. sand- och TNT-duplicering. Dessutom finns massor av externa verktyg (i form av mods, plugins och program) som kan ge spelare förmåner i spelet som Vanilla Minecraft inte erbjuder. LeCaNis spelare har gemensamt skapat ett ramverk för var gränserna ska gå.</p>
	<h3>Du har rätt till följande:</h3>
	<ul style="list-style: inside">
	<li>Ändra ljusinställningar bortom Moody till Bright</li>
	<li>Använda mods som erbjuder dynamiskt ljus (t.ex. Shaders med handburna facklor)</li>
	<li>Använda mods som underlättar ditt inventory- och kistmanagement</li>
	<li>Använda Schematic mod</li>
	<li>Använda MiniHUD mod</li>
	<li>Utnyttja TNT-dupliciering (mha slimeblock)</li>
	<li>Utnyttja fallande block-duplicering (mha enderportal)</li>
	<li>Utnyttja buggar för att bygga under y:-64 och över y:319</li>
	<li>Använda program som kan hitta biomer, strukturer och slime chunks</li>
	<li>Använda DynMap för att hitta till delar av världen</li>
	<li>Använda makron, autoklickare och Auto Hot Key</li>
	<li>Använda F3 eller liknande för att se koordinater</li>
	<li>Logga ut för att invänta hjälp nära dödsfall</li>
	<li>Utnyttja buggar som tar bort bedrock</li>
	<li>Använda Ender Pearls för att ta sig genom tak och väggar</li>
	<li>Stå AFK för att farma</li>
	</ul>
	
	<h3>Du har inte rätt till följande:</h3>
	<ul style="list-style: inside">
	<li>Utnyttja föremålsduplicering</li>
	<li>Använda buggar som modifierar verktyg och vapens egenskaper</li>
	<li>Använda mods eller externa program som direkt kan ge dig föremål</li>
	<li>Använda mods eller externa program som ger din övernaturliga fördelar (flyga, odödlighet, etc.)</li>
	<li>Använda mods som orsakar X-ray-effekter</li>
	<li>Använda vanillametoder som orsakar X-ray-effekter</li>
	</ul>
	
	<h2>Admins rättigheter</h2>
	<p>Varje admin har rätt att använda straffkommandon för att behålla ordningen på servern om så behövs.
	</p>
