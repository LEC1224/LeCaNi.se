	<h2>Spelarklasser</h2>
	<p>
	Länge leve klassamhället! Nä, skoja... eller? Men för att underlätta för alla att förstå vilka de olika spelarna är har vi olika färger på olika spelares namn. Detta gör det t.ex. lättare att hitta admins och fatta när någon är AFK. För detta används teams i Minecrafts scoreboard-system.
	</p>
	<p>
	Lagen är uppdelade enligt följande:
	<ul>
		<li><span style="background-color: white; font-weight: bold">Nya spelare</span> är vita. Man räknas som ny spelare tills att man spelat 1000 minuter (16h + 40 min). De tusen minuterna behöver inte vara i samma månad.</li>
		<li><span style="background-color: lightgreen; font-weight: bold">Vanliga spelare</span> är gröna. Detta är alla som spelat i åtminstone 1000 minuter.</li>
		<li><span style="background-color: lightblue; font-weight: bold">Admins</span> är blå och är de du ska vända dig till om du har frågor om servern eller vill be om kommandohjälp. Admins har även rätt att banna och kicka spelare som beter sig illa.</li>
		<li><span style="background-color: lightgray; font-weight: bold">AFK-spelare</span> är grå och har blivit AFK genom att antingen stå stilla i fem minuter eller använt /afk-kommandot. När man är AFK ökar inte minuträknarna för månad och livstid.</li>
		<li><span style="background-color: lightcoral; font-weight: bold">Bannade spelare</span> är röda och har blivit bannade på grund av upprepade eller grova regelbrott eller inaktivitet.</li>
