	<h2>Komma igång</h2>
	<p>
	När du spawnar befinner du dig i Fabulania. Härifrån är du fri att gå vart du vill och bygga. Det finns inga regler på att du måste bygga i städer. Det finns städer som du inte får bygga i men om det inte är skyltat så går det bra att bygga där.
	</p>
	<p>
	Du får hugga träd mer eller mindre vart du vill. Om trädet du vill hugga tydligt är menat att vara där (t.ex i en park, inhängnad eller trädgård) så ska du låta det vara och om du hugger i/nära en stad så bör du replanta men annars är du fri att göra hur du vill. Utanför städerna behöver du inte bry dig om skogen.
	</p>
	<p>
	Vid resurssamlande är det trevligt om du försöker få det att se naturligt ut, särskilt nära städer, men detta är inget måste. Det finns ett stort stenbrott (X-Mines) och en öken där du kan hämta stora mängder av sten/sand utan att bry dig. X-Mines har dock strikta regler om att man inte ska dit för att gräva efter minrealer utan sten.
	</p>
